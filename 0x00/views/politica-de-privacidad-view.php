        <h2 class="title">Política de privacidad</h2>
        <h3 class="subtitle">DATOS PERSONALES</h3>
        <p>Para el registro en el programa de voluntariado Soñadores y Hacedores.&nbsp; El usuario autoriza el
            tratamiento de sus datos personales: nombre, apellido, correo electrónico, fecha de nacimiento con día, mes
            y año, cédula, ciudad de residencia, dirección, teléfono fijo o celular, por parte de la Fundación Enfócate
            con Propósito o sus aliados al programa Soñadores y Hacedores.&nbsp; Al proporcionar esta información el
            usuario otorga su consentimiento expreso para que La Fundación Enfócate con Propósito y/o terceros que
            tengan vínculos con el programa de voluntariado Soñadores y Hacedores lleven a cabo operaciones de
            tratamiento de sus Datos Personales tales como recolección, almacenamiento, uso, circulación, o supresión,
            en los términos de la Política de Protección de Datos de La Fundación Enfócate con Propósito disponible en
            la URL http://www.enfocateconproposito.org&nbsp; Los Datos Personales serán sometidos a Tratamiento para el
            desarrollo de actividades de voluntariado desarrolladas por La Fundación Enfócate con Propósito o sus
            aliados en el programa Soñadores y Hacedores.</p>
        <p>Además, el usuario otorga su consentimiento expreso para que La Fundación Enfócate con Propósito y transfiera
            y/o transmita sus Datos Personales o permita el acceso a éstos, a terceros ubicados en Colombia o en el
            exterior, incluso a países que no proporcionen niveles adecuados de protección de Datos Personales.</p>
        <p>La Fundación Enfócate con Propósito tratará sus Datos Personales durante el tiempo que sea necesario para
            cumplir con las finalidades mencionadas y/o mientras sea necesario para el cumplimiento de obligaciones
            legales o contractuales.</p>
