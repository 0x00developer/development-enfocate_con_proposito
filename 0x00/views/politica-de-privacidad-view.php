        <p>El usuario dispondrá en todo momento de los derechos a (1) conocer, actualizar, rectificar y eliminar sus
            datos personales frente a La Fundación Enfócate con Propósito, (2) solicitar a La Fundación Enfócate con
            Propósito prueba de esta autorización, si usted la otorga, (3) ser informado por La Fundación Enfócate con
            Propósito, previa solicitud suya, sobre el uso que le ha dado a sus datos personales, (4) presentar ante la
            Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto en la presente ley y las
            demás normas que la modifiquen, adicionen o complementen, (5) revocar su autorización y/o solicitar la
            supresión de sus datos (6) acceder en forma gratuita a sus datos personales que hayan sido objeto de
            tratamiento.</p>
        <p>El usuario podrá ejercer sus derechos comunicándose al correo: info@enfocateconproposito.com, al teléfono:
            320 819 8234 en Bogotá o directamente en nuestras oficinas administrativas, ubicadas en la Calle 54 # 34 –
            05 Sur de la ciudad de Bogotá.</p>
